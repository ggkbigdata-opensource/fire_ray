package org.fire.platform.modules.area.bean;

/**
 * 微型消防站配备车辆信息
 * @author Max
 *
 */
public class Vehicle {

	
	String vehicleType;
	
	String foamLoadinge;
	
	String waterAmount;
	
	String amount;

	public String getVehicleType() {
		return vehicleType;
	}

	public void setVehicleType(String vehicleType) {
		this.vehicleType = vehicleType;
	}

	public String getFoamLoadinge() {
		return foamLoadinge;
	}

	public void setFoamLoadinge(String foamLoadinge) {
		this.foamLoadinge = foamLoadinge;
	}

	public String getWaterAmount() {
		return waterAmount;
	}

	public void setWaterAmount(String waterAmount) {
		this.waterAmount = waterAmount;
	}

	public String getAmount() {
		return amount;
	}

	public void setAmount(String amount) {
		this.amount = amount;
	}
	
	
}
