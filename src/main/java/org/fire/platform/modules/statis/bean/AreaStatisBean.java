package org.fire.platform.modules.statis.bean;

public class AreaStatisBean {
	
	private Long id;
	private Integer type;
	private String name;
	private Long confirmFireNum;
	private Long unpassReportNum;
	private Long punishNum;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Integer getType() {
		return type;
	}
	public void setType(Integer type) {
		this.type = type;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Long getConfirmFireNum() {
		return confirmFireNum;
	}
	public void setConfirmFireNum(Long confirmFireNum) {
		this.confirmFireNum = confirmFireNum;
	}
	public Long getUnpassReportNum() {
		return unpassReportNum;
	}
	public void setUnpassReportNum(Long unpassReportNum) {
		this.unpassReportNum = unpassReportNum;
	}
	public Long getPunishNum() {
		return punishNum;
	}
	public void setPunishNum(Long punishNum) {
		this.punishNum = punishNum;
	}
	
	
	
	

}
